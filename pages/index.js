import Head from 'next/head'
import { useRouter } from 'next/router'
import Image from 'next/image'
import { useEffect,useState } from 'react'
import styles from '../styles/Home.module.css'
import axios from 'axios'

const getUrl = "https://pythontemperaturetracker.herokuapp.com"
// const getUrl = "http://localhost:5000"
const logoutUrl = getUrl+"/logout"

export default function Home() {
  const router = useRouter()
  const [isMounted, setIsMounted] = useState(false)
  const [data, setData] = useState(null)
  useEffect(() => {
    setIsMounted(true)
  })
  useEffect(async () => {
    try{
      let response = await axios.get(getUrl,{withCredentials: true})
      setData(response)
      console.log(response)
      if(response.status == 201){
        router.push("/login")
      }
    }
    catch(e){
      console.log(e)
    }
  }, [isMounted])
async function logout(){
  try{
    let response = await axios.post(logoutUrl, {}, {withCredentials:true})
  }catch(e){
    console.log()
  }
}

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <button onClick={logout}>Logout</button>
      
    </div>
  )
  
}
